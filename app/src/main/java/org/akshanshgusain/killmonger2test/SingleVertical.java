package org.akshanshgusain.killmonger2test;

public class SingleVertical {
    private String mTextView;
    private int mImageView;

    public SingleVertical(String mTextView, int mImageView) {
        this.mTextView = mTextView;
        this.mImageView = mImageView;
    }

    public String getmTextView() {
        return mTextView;
    }

    public void setmTextView(String mTextView) {
        this.mTextView = mTextView;
    }

    public int getmImageView() {
        return mImageView;
    }

    public void setmImageView(int mImageView) {
        this.mImageView = mImageView;
    }
}
